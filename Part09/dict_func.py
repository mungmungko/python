# dict func
# .keys()
user1 = {'name': 'Spencer', 'colleage': '프로그래머스', 'year': 20, 'lang': 'Python'}
print(user1.keys())
print(list(user1.keys()))

# .values()
user1 = {'name': 'Spencer', 'colleage': '프로그래머스', 'year': 20, 'lang': 'Python'}
print(user1.values())
print(list(user1.values()))

# .items()
user1 = {'name': 'Spencer', 'colleage': '프로그래머스', 'year': 20, 'lang': 'Python'}
print(user1.items())
print(list(user1.items()))